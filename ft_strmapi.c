#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	size_t	l;
	char	*str;

	if (!s || !f)
		return (NULL);
	l = ft_strlen(s);
	str = (char *) malloc (sizeof(char) * (ft_strlen((char *) s) + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
